#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str)
		write(1, str++, 1);
	write(1, "\n", 1);
}

int	main(int arg, char **str)
{
	int	i;

	i = 1;
	while (i < arg)
		ft_putstr(str[i++]);
}
